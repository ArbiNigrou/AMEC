
// Classe pour représenter un produit
class Product {
  final String name; // Nom du produit
  final double price; // Prix du produit
  final String imageUrl; // URL de l'image du produit
  final String description; // Description du produit

  // Constructeur pour initialiser les propriétés
  Product({
  required this.name, 
  required this.price, 
  required this.imageUrl,
  required this.description, // Description du produit

  }); // Constructeur
}